int		check_line(char *form)
{
	int	i;

	i = 4;
	while (form[i] != '\0')
	{
		if (i % 5 == 4)
		{
			if (form[i] != '\n')
				return (0);
		}
		else if (form[i] == '\n')
			return (0);
		i++;
	}
	return (1);
}
